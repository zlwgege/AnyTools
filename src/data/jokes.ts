export function getRandomJokes(count: number = 3): string[] {
  const shuffled = [...FIXED_JOKES].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function generateRandomJoke(): string {
  const i = INDUSTRIES[Math.floor(Math.random() * INDUSTRIES.length)]
  const c = CHARACTERS[Math.floor(Math.random() * CHARACTERS.length)]
  const a = ACTIONS[Math.floor(Math.random() * ACTIONS.length)]
  const template = TEMPLATES[Math.floor(Math.random() * TEMPLATES.length)]
  return template(i, c, a)
}

export function getMixedJokes(count: number = 3): string[] {
  const jokes: string[] = []
  // 至少保证有一条固定笑话
  const fixedCount = Math.min(2, count)
  const fixed = getRandomJokes(fixedCount)
  jokes.push(...fixed)

  // 剩余用模板生成
  while (jokes.length < count) {
    const generated = generateRandomJoke()
    if (!jokes.includes(generated)) {
      jokes.push(generated)
    }
  }

  return jokes.slice(0, count)
}
